# String Functions

myStr = 'Hello There World!'

# Capitalize
print(myStr.capitalize())

# Swap Case
print(myStr.swapcase())

# Get length
print(len(myStr))

#Replace
print(myStr.replace('World', 'Everyone'))

# Count
sub ='l'
print(myStr.count(sub))

# Startswith
print(myStr.startswith('Hello'))

# Endswith
print(myStr.endswith('!'))

#split to list
print(myStr.split())

# find
print(myStr.find('e'))

# index
print(myStr.index('h'))

# Is all alphanumeric
print(myStr.isalnum())

# Is all alphabetic
print(myStr.isalpha())

# Is all numeric
print(myStr.isalnum())